#!/usr/bin/env node
// Inlines the built Fill-PDF playground island (dist/playground-form.{js,css})
// into cli/src/server/playground-form-generated.ts as base64 strings, so the
// compiled `facet` binary serves it with no runtime file dependency. Run after
// `vite build --config vite.playground.config.ts`; the generated bundle is
// committed so standalone CLI builds do not need the root playground toolchain.
const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..', '..');
const jsPath = path.join(root, 'dist', 'playground-form.js');
const cssPath = path.join(root, 'dist', 'playground-form.css');

function readB64(p) {
  if (!fs.existsSync(p)) {
    throw new Error(`gen-playground-form: ${p} not found; run pnpm run build:playground from the repository root`);
  }
  return fs.readFileSync(p).toString('base64');
}

const jsB64 = readB64(jsPath);
const cssB64 = readB64(cssPath);

const out = `// DO NOT MODIFY — auto-generated by cli/scripts/gen-playground-form.cjs.
// Base64 of the built Fill-PDF playground island (dist/playground-form.{js,css}).
// Regenerate with \`pnpm run build:playground\` from the repository root.
export const playgroundFormJsB64 = ${JSON.stringify(jsB64)};
export const playgroundFormCssB64 = ${JSON.stringify(cssB64)};
`;

const target = path.join(__dirname, '..', 'src', 'server', 'playground-form-generated.ts');
fs.writeFileSync(target, out);
console.log(`gen-playground-form: wrote ${target} (js ${jsB64.length}b64, css ${cssB64.length}b64)`);
